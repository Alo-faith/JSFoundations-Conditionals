// Define the numbers and the operation here
let first = 50;
let second = 10;
let operation = "+";
// Write if-statements here
if (operation === "/") {
  console.log(first / second);
} else if (operation === "+") {
  console.log(first + second);
} else if (operation === "-") {
  console.log(first - second);
} else if (operation === "*") {
  console.log(first * second);
} else {
  console.log("Error input");
}
